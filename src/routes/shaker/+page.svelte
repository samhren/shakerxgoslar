<div class="centered">
	Shaker Heights is a city located in Cuyahoga County, Ohio, United States. It is a suburb of
	Cleveland and is known for its beautiful residential neighborhoods, historic architecture, and
	tree-lined streets. The city was named after the Shakers, a religious sect that had a community in
	the area in the 19th century. Shaker Heights is well-regarded for its well-planned layout,
	designed by landscape architects, resulting in a unique and aesthetically pleasing environment.
	The city is known for its diverse housing styles, including many elegant, large homes.
	Additionally, Shaker Heights has a commitment to education, and it is home to the Shaker Heights
	City School District, which has a reputation for academic excellence (and the best German
	program).
</div>

